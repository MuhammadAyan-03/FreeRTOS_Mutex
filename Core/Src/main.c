/**
  * @brief  Period elapsed callback in non blocking mode
  * @note   This function is called  when TIM6 interrupt took place, inside
  * HAL_TIM_IRQHandler(). It makes a direct call to HAL_IncTick() to increment
  * a global variable "uwTick" used as application time base and for TIM7, it checks
  * the button state.
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Instance == TIM6)
	{
		HAL_IncTick();
	}
	if (htim->Instance == TIM7)
	{
		HAL_TIM_Base_Stop_IT(&htim7);
		GPIO_PinState state = HAL_GPIO_ReadPin(BTN_Port, BTN_Pin); //check button state
		buttonstate = (state == GPIO_PIN_RESET); // 1 = pressed, 0 = released
	}
}
/**
  * @brief HAL_GPIO_EXTI_Callback Function
  * @param None
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if (GPIO_Pin == BTN_Pin)
	{
		__HAL_TIM_SET_COUNTER(&htim7, 0); //starts TIM7
		HAL_TIM_Base_Start_IT(&htim7);
	}
}

